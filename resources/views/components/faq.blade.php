<script>
  const accordionButtons = document.querySelectorAll('[data-accordion-target]'); // Select all buttons

  accordionButtons.forEach((button) => {
    button.addEventListener('click', () => {
      const targetId = button.dataset.accordionTarget; // Get target body ID
      const targetBody = document.getElementById(targetId.slice(1)); // Remove leading #
      const plusIcon = button.querySelector('.plus');
      const minusIcon = button.querySelector('.minus');

      const isHidden = targetBody.classList.contains('hidden');

      targetBody.classList.toggle('hidden'); // Toggle visibility

      // Toggle icon visibility based on collapse state
      plusIcon.classList.toggle('hidden', isHidden);
      minusIcon.classList.toggle('hidden', !isHidden);
    });
  });
</script>
